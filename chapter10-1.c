/* This program increase a conter form 1 to 5 printing updates
    and then counts down back to one*/
    
#include <stdio.h>

int main(void)
{
    int ctr = 0;
    ctr = ctr + 1;//increase counter by one
    printf("Counter is at %d.\n", ctr);
    ctr = ctr + 1;
    printf("Counter is at %d.\n", ctr);
    ctr = ctr + 1;
    printf("Counter is at %d.\n", ctr);
    ctr = ctr + 1;
    printf("Counter is at %d.\n", ctr);
    ctr = ctr +1;
    printf("Counter is at %d.\n", ctr);
    ctr = ctr - 1;
    printf("Counter is at %d.\n", ctr);
    ctr = ctr - 1;
    printf("Counter is at %d.\n", ctr);
    ctr = ctr - 1;
    printf("Counter is at %d.\n", ctr);
    ctr = ctr - 1;
    printf("Counter is at %d.\n", ctr);
    ctr = ctr - 1;
    printf("Counter is at %d.\n", ctr);
    return 0;
}    
